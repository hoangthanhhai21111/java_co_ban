package phepToan1Ngoi;

public class vidu {
    public static void main(String[] args) {
        int a = 5;
        boolean b = false;

        System.out.println("a = "+ a);       
        System.out.println("-a = "+ -a);    
        System.out.println("+a = "+ +a);
        System.out.println("--a = "+ --a);
        System.out.println("a++ = "+ a++);        
        System.out.println("!b = "+ !b);
    }
}
